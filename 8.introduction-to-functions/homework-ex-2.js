// Function declaration to add two numbers
function addNumbers(num1, num2) {
    return num1 + num2;
}

// Add and log the results
console.log(`The sum of 3 and 7 is: ${addNumbers(3, 7)}`);    // Output: 10
console.log(`The sum of 10 and 5 is: ${addNumbers(10, 5)}`);  // Output: 15
console.log(`The sum of 0 and 0 is: ${addNumbers(0, 0)}`);    // Output: 0

// Final Output:
// The sum of 3 and 7 is: 10
// The sum of 10 and 5 is: 15
// The sum of 0 and 0 is: 0