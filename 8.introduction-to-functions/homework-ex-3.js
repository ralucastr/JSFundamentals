// Function expression to multiply two numbers
const multiplyNumbers = function(num1, num2) {
    return num1 * num2;
};

// Multiply and log the results
console.log(`The product of 4 and 5 is: ${multiplyNumbers(4, 5)}`); // Output: 20
console.log(`The product of 7 and 3 is: ${multiplyNumbers(7, 3)}`); // Output: 21
console.log(`The product of 6 and 2 is: ${multiplyNumbers(6, 2)}`); // Output: 12

// Final Output:
// The product of 4 and 5 is: 20
// The product of 7 and 3 is: 21
// The product of 6 and 2 is: 12
